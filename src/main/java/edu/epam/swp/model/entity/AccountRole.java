package edu.epam.swp.model.entity;

/**
 * The enum contains account's roles used in the application.
 * @author romab
 */
public enum AccountRole {
    /**
     * User account role.
     */
    USER,
    /**
     * Admin account role.
     */
    ADMIN,
    /**
     * Inactive account role.
     */
    INACTIVE,
    /**
     * Blocked account role.
     */
    BLOCKED,
    /**
     * Guest account role.
     */
    GUEST,
}
